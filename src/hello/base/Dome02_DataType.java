package hello.base;

public class Dome02_DataType {
    public static void main(String[] args) {
        // 八大基本数据类型

        // 整数
        //最常用
        int num1 = 10;
        byte num2 = 20;
        short num3 = 30;
        //Long类型要在数字后面加个L
        long num4 = 30L;

        // 小数:浮点数
        //默认是double,float类型要在后面加L
        float num5 = 50.1F;
        double num6 = 3.13290123;

        // 字符
        char name = '国';
        // 字符串,string不是关键字,是类
        // string namea = "中国";

        // 布尔值
        boolean flag = true;
    }
}
